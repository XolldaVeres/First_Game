print("=== Василий Осипов ===")
print("=== Моя первая игра ===")
print("Ты просыпаешься в незнакомой комнате. Что будешь делать?")

inventory = []
room = "start"
hunger = 100

while True:
    hunger -= 5
    print(f"🍗 Голод: {hunger}")
    
    if hunger <= 0:
        print("Ты умер от голода... Игра окончена")
        break

    if room == "start":
        action = input("\n> ").lower()
        
        if action == "осмотреться":
            print("Ты в мрачной комнате. Вокруг: старая дверь, пыльный сундук, запертое окно и странный человек в углу.")
        
        elif action == "поесть":
            if "хлеб" in inventory:
                hunger = 100
                print("Ты поел. Голод утолен!")
            else:
                print("Нечего есть. Найди еду в сундуке.")
        
        elif action == "пойти в коридор":
            room = "hall"
            print("Ты вышел в темный коридор. Сыро и страшновато.")
        
        elif action == "осмотреть сундук":
            if "ключ" in inventory:
                print("Сундук уже открыт. Там пусто.")
            else:
                print("Ты открываешь старый сундук. Внутри блестят ключ и молот!")
                print("Ты забрал ключ, молот и хлеб!")
                inventory.append("ключ")
                inventory.append("молот")
                inventory.append("хлеб")
        
        elif action == "осмотреть дверь":
            if "ключ" in inventory:
                print("Ты вставляешь ключ в замочную скважину. Дверь со скрипом открывается.")
                print("За дверью оказывается густой лес. Ты делаешь шаг вперед...")
                room = "jungle"
            else:
                print("Дверь заперта. Нужен ключ.")
        
        elif action == "осмотреть окно":
            if "молот" in inventory:
                print("Ты разбиваешь окно молотом и выпрыгиваешь наружу.")
                print("Ты приземлился в кусты и выбрался на свободу!")
                print("=== ТЫ ПОБЕДИЛ! ===")
                break
            else:
                print("Ты смотришь в окно. Стекло крепкое, голыми руками не разбить.")
        
        elif action == "подойти к мужику":
            print("Человек поднимает голову. Это серийный убийца! Он бросается на тебя...")
            print("Ты погиб.")
            print("=== ИГРА ОКОНЧЕНА ===")
            break
        
        elif action == "инвентарь":
            if inventory:
                print(f"В твоем рюкзаке: {', '.join(inventory)}")
            else:
                print("Рюкзак пуст. Нужно что-то найти.")
        
        else:
            print("Неизвестная команда. Доступно: осмотреться, пойти в коридор, осмотреть сундук, осмотреть дверь, осмотреть окно, подойти к мужику, инвентарь, поесть")
    
    elif room == "hall":
        action = input("\n[Коридор] > ").lower()
        
        if action == "осмотреться":
            print("Длинный темный коридор. На стене висит старая картина с пустыми глазами. В конце коридора дверь.")
        
        elif action == "поесть":
            if "хлеб" in inventory:
                hunger = 100
                print("Ты поел. Голод утолен!")
            else:
                print("Нечего есть. Найди еду в сундуке.")
        
        elif action == "вернуться":
            room = "start"
            print("Ты вернулся обратно в комнату.")
        
        elif action == "пойти к двери":
            print("Ты подходишь к двери в конце коридора. Она ведет в лес.")
            room = "jungle"
        
        elif action == "инвентарь":
            if inventory:
                print(f"В твоем рюкзаке: {', '.join(inventory)}")
            else:
                print("Рюкзак пуст.")
        
        elif action == "выход":
            print("Ты решил сдаться. Игра окончена.")
            break
        
        else:
            print("Неизвестно. Можно: осмотреться, вернуться, пойти к двери, инвентарь, поесть, выход")
    
    elif room == "jungle":
        action = input("\n[Лес] > ").lower()
        
        if action == "осмотреться":
            print("Ты стоишь в густом лесу. Вокруг высокие деревья. Видно: статую русалки, старый сарай и тропинку к часовне.")
        
        elif action == "поесть":
            if "хлеб" in inventory:
                hunger = 100
                print("Ты поел. Голод утолен!")
            else:
                print("Нечего есть. Найди еду в сундуке.")
        
        elif action == "осмотреть статую":
            if "карта" in inventory:
                print("Ты уже брал карту у русалки. Она молча смотрит на тебя.")
            else:
                print("Ты подходишь к белой статуе русалки. В ее руке зажата старая карта.")
                print("Ты забираешь карту.")
                inventory.append("карта")
        
        elif action == "осмотреть сарай":
            print("Старый покосившийся сарай. Внутри пусто, только пахнет сеном.")
        
        elif action == "пойти к часовне":
            print("Ты идешь по тропинке к старой часовне. Внутри темно и тихо. Ничего интересного.")
        
        elif action == "посмотреть карту":
            if "карта" in inventory:
                print("На карте отмечено: дом, из которого ты вышел, старая часовня, деревня слева, кладбище справа.")
                print("Крестиком помечено место у статуи русалки — возможно, там что-то зарыто.")
            else:
                print("У тебя нет карты. Найди статую русалки.")
        
        elif action == "вернуться":
            room = "start"
            print("Ты возвращаешься обратно в комнату через дверь.")
        
        elif action == "инвентарь":
            if inventory:
                print(f"В твоем рюкзаке: {', '.join(inventory)}")
            else:
                print("Рюкзак пуст.")
        
        elif action == "выход":
            print("Ты решил остаться в лесу навсегда. Игра окончена.")
            break
        
        else:
            print("Неизвестно. Можно: осмотреться, осмотреть статую, осмотреть сарай, пойти к часовне, посмотреть карту, вернуться, инвентарь, поесть, выход")